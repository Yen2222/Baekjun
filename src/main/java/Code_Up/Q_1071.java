package Code_Up;

import java.util.Scanner;

public class Q_1071 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        while (true){
            int a = sc.nextInt();
            if(a!=0){
                System.out.println(a);
            }
            if(a==0){
                break;
            }
        }
    }
}
